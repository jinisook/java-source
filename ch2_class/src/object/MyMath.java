package object;

// 이름은 동일해도 되지만, 괄호 안은 다르게
public class MyMath {

    long add(long a, long b) {
        return a + b;
    }

    long subtract(long a, long b) {
        return a - b;
    }

    long multiplay(long a, long b) {
        return a * b;
    }

    long divide(long a, long b) {
        return a / b;
    }
}
